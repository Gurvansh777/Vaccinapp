package com.user.mareez.model;

public class UserVaccinationInfo {
	private String fname, vaccinType, notes, vaccinDate;
	private int vaccinId;

	public String getFname() {
		return fname;
	}

	public void setFname(String fname) {
		this.fname = fname;
	}

	public String getVaccinType() {
		return vaccinType;
	}

	public void setVaccinType(String vaccinType) {
		this.vaccinType = vaccinType;
	}

	public String getNotes() {
		return notes;
	}

	public void setNotes(String notes) {
		this.notes = notes;
	}

	public String getVaccinDate() {
		return vaccinDate;
	}

	public void setVaccinDate(String vaccinDate) {
		this.vaccinDate = vaccinDate;
	}

	public int getVaccinId() {
		return vaccinId;
	}

	public void setVaccinId(int vaccinId) {
		this.vaccinId = vaccinId;
	}
	
	

}
